      SUBROUTINE ENDA
C=======================================================================
C
C     MOVING AND SAVING OF POINTS DURING SUMMATION ARE PERFORMED BY
C     THIS ROUTINE.
C
C     ADDITION OF NEXT SECTION IS COMPLETE. LEAVE A IN CORE OR ON
