      IF(WEIGHT.EQ.1.0) GO TO 20
      II=I+NPAGEM1
      IF(II.GT.N2A) II=N2A
      III=(II-I)+1
      DO 10 J=1,III
   10 YA(J)=WEIGHT*YA(J)
   20 IF(N2A.LE.NPAGE) GO TO 40
      WRITE(ISCRA) XA,YA
   30 CONTINUE
      END FILE ISCRA
      REWIND ISCRA
   40 RETURN
      END
