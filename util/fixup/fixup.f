      SUBROUTINE NEED(MAKE1,MAKE2,MT,MTLIST)
C=======================================================================
C
C     DETERMINE IF MT IS REQUIRED BY RECONSTRUCTION REQUESTS NR1 THROUGH
C     NR2. IF MT IS REQUIRED RETURN MTLIST=1 (ADD) OR =-1 (SUBTRACT),
C     OTHERWISE RETURN =0 (NO REQUIRED).
C
C=======================================================================
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      SAVE
      COMMON/MAKER/MAKE,MTMAKE(20),MTRANG(20),MTADD(3,10,20)
      COMMON/WAITER/WEIGHT
      COMMON/OHRATS/MYRATS
C-----SET UP LOOP OVER REQUESTS.
      DO 40 MR=MAKE1,MAKE2
C-----SET UP LOOP OVER MT RANGES.
      NRANGE=MTRANG(MR)
      DO 30 NR=1,NRANGE
C-----FIRST CHECK FOR RATIO DATA.
      IF(MTADD(3,NR,MR).EQ.0) GO TO 10
      IF(MT.EQ.MTADD(1,NR,MR).OR.MT.EQ.MTADD(2,NR,MR)) GO TO 50
      GO TO 30
C-----NEXT CHECK FOR RANGES.
   10 IF(MT-IABS(MTADD(1,NR,MR))) 30,50,20
   20 IF(MT-IABS(MTADD(2,NR,MR))) 50,50,30
   30 CONTINUE
   40 CONTINUE
C-----MT IS NOT NEEDED.
      MTLIST=0
      GO TO 70
C-----MT IS NEEDED. DEFINE MT NUMBER FOR OUTPUT (ADD = MT, MINUS =-MT)
C-----AND WEIGHT (ADD = 1.0, MINUS = -1.0).
   50 WEIGHT=1.0
      MTLIST=MT
      IF(MTADD(1,NR,MR).GE.0) GO TO 60
      WEIGHT=-1.0
      MTLIST=-MT
C-----DEFINE RESULT AS EITHER SUM (MYRATS=0), PRODUCT (MYRATS=4) OR
C-----RATIO (OTHERWISE = + OR - 3)
   60 MYRATS=0
      IF(MTADD(3,NR,MR).NE.0) MYRATS=MTADD(3,NR,MR)
   70 RETURN
      END
      SUBROUTINE ADDB2C
C=======================================================================
C
C     COMBINE A SECTION FROM B AND C AND STORE THE RESULT IN A.
C
C     HOW B AND C ARE COMBINED WILL DEPEND ON MYRATS
C
C     MYRATS = 0 - A = B + C
C            = 1 - A = B / C
C
C     WHEN DEFINING RATIOS IF THE DENOMINATOR IS ZERO AND THE NUMERATOR
C     IS NON-ZERO THE RATIOS WILL BE DEFINED TO BE ZERO.
C
C=======================================================================
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      SAVE
      COMMON/TABA/I2A,N2A,IPA1,IPA2
      COMMON/TABB/I2B,N2B,IPB1,IPB2
      COMMON/TABC/I2C,N2C,IPC1,IPC2
      COMMON/ENDSA/XANOW,XALAST,YANOW,YALAST
      COMMON/ENDSB/XBNOW,XBLAST,YBNOW,YBLAST
      COMMON/ENDSC/XCNOW,XCLAST,YCNOW,YCLAST
      COMMON/OHRATS/MYRATS
C-----INITIALIZE POINT INDICES.
      I2B=-1
      I2C=-1
      N2A=-1
C-----READ FIRST POINT FROM B AND C.
      CALL GETB
      CALL GETC
C-----SELECT LOWER ENERGY.
   10 IF(XBNOW-XCNOW) 20,110,150
C
C     E-B IS LESS. IF C STARTS AT A HIGHER ENERGY XC-A = XC-B. IF
C     NOT, INTERPOLATE C TO DEFINE XC-C AT E-B.
C
   20 XANOW=XBNOW
      IF(I2C-1) 30,30,70
   30 IF(MYRATS) 50,40,50
   40 YANOW=YBNOW
      GO TO 60
   50 YANOW=0.0
   60 CALL GETB
      GO TO 240
   70 YCTERP=((XBNOW-XCLAST)*YCNOW+(XCNOW-XBNOW)*YCLAST)/(XCNOW-XCLAST)
      IF(MYRATS) 90,80,90
   80 YANOW=YBNOW+YCTERP
      GO TO 100
   90 CALL RATIOS(YANOW,YBNOW,YCTERP)
  100 CALL GETB
      GO TO 240
C
C     ENERGIES ARE EQUAL. NO INTERPOOLATION REQUIRED.
C
  110 XANOW=XBNOW
      IF(MYRATS) 130,120,130
  120 YANOW=YBNOW+YCNOW
      GO TO 140
  130 CALL RATIOS(YANOW,YBNOW,YCNOW)
  140 CALL GETB
      CALL GETC
      GO TO 240
C
C     E-C IS LESS. IF B STARTS AT A HIGHER ENERGY XC-A = XC-C. IF
C     NOT, INTERPOLATE B TO DEFINE XC-B AT E-C.
C
  150 XANOW=XCNOW
      IF(I2B-1) 160,160,200
  160 IF(MYRATS) 180,170,180
  170 YANOW=YCNOW
      GO TO 190
  180 YANOW=0.0
  190 CALL GETC
      GO TO 240
  200 YBTERP=((XCNOW-XBLAST)*YBNOW+(XBNOW-XCNOW)*YBLAST)/(XBNOW-XBLAST)
      IF(MYRATS) 220,210,220
  210 YANOW=YBTERP+YCNOW
      GO TO 230
  220 CALL RATIOS(YANOW,YBTERP,YCNOW)
  230 CALL GETC
C
C     SAVE A. CONTINUE UNTIL ALL POINTS HAVE BEEN USED.
C
  240 CALL SAVEA
      IF(I2B.LE.N2B.OR.I2C.LE.N2C) GO TO 10
C
C     SUMMATION IS FINISHED. IF REQUIRED COPY LAST PAGE OF A TO SCRATCH
C     ISCRA.
C
      CALL ENDA
      RETURN
      END
      SUBROUTINE RATIOS(YAIN,YBIN,YCIN)
C=======================================================================
C
C     DEFINE RATIO OR PRODUCT
C     MYRATS = 4         - PRODUCT - YA = YB*YC
C            = OTHERWISE - RATIO   - YA = YB/YC
C
C     FOR RATIO, IF MYRATS IS LESS THAN ZERO REVERSE TO DEFINE
C     YA = YC/YB
C
C     IF YC = 0 - AND YB = 0 - YA = 1
C           = 0 - OTHERWISE  - YA = 0
C
C=======================================================================
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      SAVE
      COMMON/OHRATS/MYRATS
C
C     PRODUCT.
C
      IF(MYRATS.NE.4) GO TO 10
      YAIN=YBIN*YCIN
      RETURN
C
C     RATIO - DIRECT OR INVERSE.
C
   10 IF(MYRATS) 30,20,20
C-----YA = YB/YC
   20 YBUSE=YBIN
      YCUSE=YCIN
      GO TO 40
C-----YA = YC/YB
   30 YBUSE=YCIN
      YCUSE=YBIN
   40 IF(YCUSE) 80,50,80
   50 IF(YBUSE) 70,60,70
C-----BOTH YC AND YB ARE ZERO
   60 YAIN=1.0
      RETURN
C-----YC IS ZERO AND YB IS NOT ZERO
   70 YAIN=0.0
      RETURN
C-----YC IS NOT ZERO
   80 YAIN=YBUSE/YCUSE
      RETURN
      END
      SUBROUTINE GETA
C=======================================================================
C
C     DEFINE NEXT DATA POINT FROM ENDF/B FORMATTED FILE.
C     READ DATA A PAGE AT A TIME FROM UNIT ITAPE.
C
C=======================================================================
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      SAVE
      INTEGER OUTP,OTAPE
      CHARACTER*1 FIELD6
      COMMON/ENDFIO/INP,OUTP,ITAPE,OTAPE
      COMMON/HEADER/C1H,C2H,L1H,L2H,N1H,N2H,MATH,MFH,MTH,NOSEQ
      COMMON/TABA/I2A,N2A,IPA1,IPA2
      COMMON/ENDSA/XANOW,XALAST,YANOW,YALAST
      COMMON/OPS/IMOPS(14)
      COMMON/MAXIE/XMAX,X20MEV
      COMMON/THRES/ETHRES
      COMMON/THRESN/NOTHRS,NOTAB(2,20)
      COMMON/LASTE/ELAST
      COMMON/FIELDC/FIELD6(11,6)
      COMMON/PAGER/NPAGE,NPAGEM1
      INCLUDE 'fixup.h'
      DATA OKDIFF/1.0D-09/
C-----INITIALIZE WHEN FIRST POINT IS REQUESTED.
      IF(I2A) 10,170,170
C
C     DEFINE FIRST POINT. FOR NON-THRESHOLD REACTIONS THIS IS ALWAYS
C     THE FIRST POINT READ. FOR THRESHOLD REACTIONS THIS IS THE FIRST
C     POINT AT OR ABOVE THE THRESHOLD AND THE CROSS SECTION MUST BE
C     EQUAL TO ZERO.
C
C-----INITIALIZE INDICES AND LOAD FIRST PAGE.
   10 I2A=1
      IPA1=0
      IPA2=NPAGE
C-----INITIALIZE LAST ENERGY
      ELAST=0.0D+00
      IF(IPA2.GT.N2A) IPA2=N2A
      IPA3=IPA2-IPA1
      CALL POINTI(XA,YA,IPA3)
C-----INITIALIZE LAST POINT TO ZERO ENERGY AND CROSS SECTION.
      XALAST=0.0
      YALAST=0.0
C-----DEFINE FIRST POINT AT OR ABOVE THRESHOLD.
      XANOW=XA(I2A)
      YANOW=YA(I2A)
C
C     THRESHOLD REACTION TREATMENT.
C
      IF(ETHRES) 280,280,20
C-----IF THRESHOLD ENERGY SHOULD NOT BE CHANGED FOR THIS REACTION ONLY
C-----PRINT WARNING MESSAGE IF FIRST POINT DOES NOT AGREE WITH THRESHOLD
   20 IF(NOTHRS.LE.0) GO TO 70
      DO 30 I=1,NOTHRS
      IF(MTH.GE.NOTAB(1,I).AND.MTH.LE.NOTAB(2,I)) GO TO 40
   30 CONTINUE
      GO TO 70
   40 IF(DABS(XANOW-ETHRES).LE.0.0001*ETHRES) ETHRES=XANOW
      IF(XANOW-ETHRES) 50,280,60
   50 CALL OUT9(XANOW ,FIELD6(1,1),3)
      CALL OUT9(ETHRES,FIELD6(1,2),3)
      WRITE(OUTP,420) ((FIELD6(M,J),M=1,11),J=1,2)
      GO TO 280
   60 CALL OUT9(XANOW ,FIELD6(1,1),3)
      CALL OUT9(ETHRES,FIELD6(1,2),3)
      WRITE(OUTP,410) ((FIELD6(M,J),M=1,11),J=1,2)
      GO TO 280
C
C     SKIP POINTS UNTIL POINT CLOSE TO OR HIGHER THAN THRESHOLD IS
C     FOUND.
C
   70 IF(DABS(XANOW-ETHRES).LE.0.0001*ETHRES) ETHRES=XANOW
      IF(XANOW-ETHRES) 80,100,130
C-----POINT IS BELOW THRESHOLD. SKIP TO NEXT POINT.
   80 I2A=I2A+1
C-----DO NOT LOOK BEYOND END OF TABLE
      IF(I2A.GE.IPA3) GO TO 90
      XANOW=XA(I2A)
      YANOW=YA(I2A)
      GO TO 70
C
C     REACTION ENDS BELOW THRESHOLD - CANNOT CORRECT THRESHOLD.
C
   90 XANOW=XA(IPA3)
      CALL OUT9(XANOW ,FIELD6(1,1),3)
      CALL OUT9(ETHRES,FIELD6(1,2),3)
      WRITE(OUTP,430) ((FIELD6(M,J),M=1,11),J=1,2)
C-----RE-INITIALIZE TO START OF TABLE.
      I2A=1
      XALAST=0.0
      YALAST=0.0
      XANOW=XA(I2A)
      YANOW=YA(I2A)
      GO TO 280
C
C     POINT CLOSE TO THRESHOLD FOUND.
C
C-----SET CROSS SECTION EQUAL TO ZERO.
  100 YANOW=0.0
C-----PRINT WARNING IF INPUT CROSS SECTION IS NOT ZERO AT THRESHOLD.
      IF(YA(I2A)) 120,120,110
  110 CALL OUT9(XANOW ,FIELD6(1,1),3)
      WRITE(OUTP,360) (FIELD6(M,1),M=1,11)
C-----LIST DELETED POINTS PRECEDING THRESHOLD (IF ANY).
  120 I2AP=I2A-1
      GO TO 140
C
C     POINT ABOVE THRESHOLD FOUND.
C
C-----DEFINE POINT AT THRESHOLD ENERGY WITH ZERO CROSS SECTION.
  130 XANOW=ETHRES
      YANOW=0.0
C-----LIST NEW THRESHOLD ENERGY.
      CALL OUT9(XANOW ,FIELD6(1,1),3)
      WRITE(OUTP,350) (FIELD6(M,1),M=1,11)
C-----CURRENT POINT IS ABOVE THRESHOLD. RESET INDEX TO USE IT THE NEXT
C-----TIME THAT THIS ROUTINE IS CALLED, UNLESS IT IS CLOSE TO THE
C-----THRESHOLD (WITHIN 1 PER-CENT) AND THE CROSS SECTION IS ZERO.
      IF(DABS(XA(I2A)-ETHRES).GT.0.01*ETHRES.OR.YA(I2A).GT.0.0)
     1 I2A=I2A-1
      I2AP=I2A
C
C     LIST DELETED POINTS PRECEDING THRESHOLD (IF ANY).
C
  140 IF(I2AP) 280,280,150
  150 CALL OUT9(XANOW ,FIELD6(1,1),3)
      WRITE(OUTP,330) (FIELD6(M,1),M=1,11)
      DO 160 J=1,I2AP
      CALL OUT9(XA(J) ,FIELD6(1,1),3)
  160 WRITE(OUTP,340) (FIELD6(M,1),M=1,11)
      GO TO 280
C
C     DEFINE NEXT POINT. NEXT POINT WILL BE THE NEXT POINT READ UNTIL
C     ALL POINTS HAVE BEEN USED. AFTER ALL POINTS HAVE BEEN USED THE
C     CROSS SECTION WILL BE CONTINUED TO HIGHER ENERGIES AS CONSTANT
C     WITH THE CROSS SECTION EQUAL TO THE LAST TABULATED VALUE OR
C     ZERO (INPUT OPTION). IF POINTS STOP BELOW 20 MEV THE CROSS SECTION
C     WILL BE EXTENDED TO 20 MEV AS CONSTANT.
C
C-----INCREMENT POINT INDEX AND SAVE LAST POINT VALUES.
  170 I2A=I2A+1
      XALAST=XANOW
      YALAST=YANOW
C-----IF NO POINTS REMAIN CONTINUE CROSS SECTION AS HISTOGRAM FROM
C-----LAST POINT.
      IF(I2A-N2A) 250,250,180
C-----EXTEND CROSS SECTION AS CONSTANT EQUAL TO EITHER LAST VALUE OR
C-----ZERO (INPUT OPTION).
  180 IF(I2A-(N2A+1)) 190,190,210
  190 IF(IMOPS(3)-1) 200,200,220
C-----EXTEND CROSS SECTION AS ZERO (UNLESS IT IS ALREADY ZERO).
  200 IF(YANOW.EQ.0.0) GO TO 210
      YANOW=0.0
      GO TO 280
C-----IF LAST ENERGY WAS BELOW 20 MEV EXTEND CROSS SECTION TO 20 MEV
C-----AND INCREMENT COUNT OF POINTS READ TO INCLUDE THIS POINT IN
C-----TABLE (CROSS SECTION IS ALREADY DEFINED).
  210 IF(IMOPS(3)) 240,220,220
  220 IF(XANOW-X20MEV) 230,240,240
  230 CALL OUT9(XANOW ,FIELD6(1,1),3)
      CALL OUT9(X20MEV,FIELD6(1,2),3)
      CALL OUT9(YANOW ,FIELD6(1,3),3)
      WRITE(OUTP,380) ((FIELD6(M,J),M=1,11),J=1,3)
      XANOW=X20MEV
      N2A=N2A+1
      GO TO 280
C-----EXTEND FOLLOWING POINTS TO KEEP CROSS SECTON AND SET ENERGY
C-----TO MAXIMUM (E.G. 100 MEV).
  240 XANOW=XMAX
      GO TO 280
C-----POINTS REMAIN. ARE THERE ANY POINTS LEFT IN CORE.
  250 IF(I2A-IPA2) 270,270,260
C-----NO. INCREMENT INDICES AND LOAD NEXT PAGE.
  260 IPA1=IPA2
      IPA2=IPA2+NPAGE
      IF(IPA2.GT.N2A) IPA2=N2A
      IPA3=IPA2-IPA1
      CALL POINTI(XA,YA,IPA3)
C-----DEFINE INDEX TO CORE AND THEN DEFINE X AND Y VALUES.
  270 ICORE=I2A-IPA1
      XANOW=XA(ICORE)
      YANOW=YA(ICORE)
C-----INSURE CROSS SECTION IS NOT NEGATIVE (INPUT OPTION).
  280 IF(IMOPS(6).LE.0.OR.YANOW.GE.0.0) GO TO 290
      CALL OUT9(XANOW ,FIELD6(1,1),3)
      CALL OUT9(YANOW ,FIELD6(1,2),3)
      WRITE(OUTP,370) ((FIELD6(M,J),M=1,11),J=1,2)
      YANOW=0.0
C-----DELETE POINTS NOT IN ASCENDING ENERGY ORDER (INPUT OPTION).
  290 IF(IMOPS(7).LE.0.OR.(XANOW.GE.XALAST)) GO TO 310
C-----ALLOW FOR SMALL DIFFERENCES (TO ABOUT 9 DIGITS)
      IF(DABS(XALAST-XANOW).LE.OKDIFF*XALAST) GO TO 300
      CALL OUT9(XALAST,FIELD6(1,1),3)
      CALL OUT9(XANOW ,FIELD6(1,2),3)
      WRITE(OUTP,390) ((FIELD6(M,J),M=1,11),J=1,2)
      GO TO 170
  300 XANOW=XALAST
C-----DELETE DUPLICATE POINTS (INPUT OPTION).
  310 IF(IMOPS(8).LE.0) GO TO 320
      IF(XANOW.NE.XALAST.OR.YANOW.NE.YALAST) GO TO 320
      CALL OUT9(XALAST,FIELD6(1,1),3)
      WRITE(OUTP,400) (FIELD6(M,1),M=1,11)
      GO TO 170
  320 RETURN
  330 FORMAT(39X,' E=',11A1,' eV. Deleted Points Preceding'/
     1 58X,'or Near Threshold.')
  340 FORMAT(57X,' Deleted ',11A1,' eV.')
  350 FORMAT(39X,' E=',11A1,' eV. Inserted Point at Threshold'/
     1 58X,'with Cross Section = 0.')
  360 FORMAT(39X,' E=',11A1,' eV. Set Cross Section = 0 at',
     1 ' Threshold.')
  370 FORMAT(39X,' E=',11A1,' eV. Cross Section=',11A1,
     1 ' (Set to Zero)')
  380 FORMAT(39X,' E=',11A1,' to',11A1,' eV.',
     1 ' Cross Section Extended=',11A1)
  390 FORMAT(39X,' E=',11A1,' Next Point E=',11A1,' Deleted')
  400 FORMAT(39X,' E=',11A1,' Duplicate Point Deleted')
  410 FORMAT(39X,' E=',11A1,' WARNING...Reaction Starts Above'/
     1 58X,'Threshold=',11A1,' eV')
  420 FORMAT(39X,' E=',11A1,' WARNING...Reaction Starts Below'/
     1 58X,'Threshold=',11A1,' eV')
  430 FORMAT(39X,' E=',11A1,' WARNING...Reaction Ends Below'/
     1 58X,'Threshold=',11A1,' eV'/
     2 58X,'Cannot Correct Threshold.'/
     3 58X,'Suggest That YOU Delete This Reaction.')
      END
