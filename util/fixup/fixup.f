      IF(MTX-MTC) 20,50,30
   20 REWIND ISCRC
      MTC=1
      GO TO 10
C-----SKIP SECTION.
   30 N2=N2TABC(MTC)
      MTC=MTC+1
      IF(N2.LE.0) GO TO 10
      DO 40 I=1,N2,NPAGE
   40 READ(ISCRC) XC,YC
      GO TO 10
C-----INCREMENT INDEX TO INDICATE THAT NEXT READ WILL READ ONE SECTION.
   50 MTC=MTC+1
      RETURN
      END
