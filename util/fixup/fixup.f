      IF(MTC.GT.MTCX) GO TO 20
C-----SKIP SECTION HEADER INFORMATION.
   10 READ(ISCRC) C1H,C2H,L1H,L2H,N1H,N2H,MATH,MFH,MTH,
     1 C1,C2,L1,L2,N1,N2
